from random import choice


# Given a list of things, we can randomly select a single item. This list
# could be anything: numbers, names, colors, restaurants...
items = [
    "Apples",
    "Bananas",
    "Grapes",
    "Kiwi",
    "Oranges",
    "Peaches",
    "Pears",
    "Plums",
    "Strawberries",
    "Watermelon",
]

# Print a random number from zero to ten (inclusive)
print(choice(items))
